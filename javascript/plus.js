function plus(num1,num2){
    let result;
    result=num1+num2; 
    return result
}
function subtraction(num1,num2){
    let result;
    result=num1-num2; 
    return result
}
function multiplication(num1,num2){
    let result;
    result=num1*num2; 
    return result
}
function division(num1,num2){
    let result;
    result=num1/num2; 
    return result
}